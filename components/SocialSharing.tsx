/**
 * קומפוננטה לשיתוף ברשתות חברתיות
 * כוללת כפתורי שיתוף ל-Facebook, WhatsApp, Twitter, LinkedIn
 */

'use client'

import { usePathname } from 'next/navigation'
import { seoConfig } from '@/config/seo.config'

interface SocialSharingProps {
  title?: string
  description?: string
  image?: string
  className?: string
  variant?: 'horizontal' | 'vertical'
}

export default function SocialSharing({
  title,
  description,
  className = '',
  variant = 'horizontal',
}: SocialSharingProps) {
  const pathname = usePathname()
  const url = `${seoConfig.siteUrl}${pathname}`
  const shareTitle = title || seoConfig.defaultTitle
  const shareDescription = description || seoConfig.defaultDescription

  const shareLinks = {
    facebook: `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(url)}`,
    whatsapp: `https://wa.me/?text=${encodeURIComponent(`${shareTitle} ${url}`)}`,
    twitter: `https://twitter.com/intent/tweet?text=${encodeURIComponent(shareTitle)}&url=${encodeURIComponent(url)}`,
    linkedin: `https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(url)}`,
    email: `mailto:?subject=${encodeURIComponent(shareTitle)}&body=${encodeURIComponent(`${shareDescription}\n\n${url}`)}`,
  }

  const handleShare = (platform: keyof typeof shareLinks) => {
    const shareUrl = shareLinks[platform]
    
    if (platform === 'whatsapp' && typeof window !== 'undefined' && window.navigator.share) {
      // Use native share API on mobile
      window.navigator.share({
        title: shareTitle,
        text: shareDescription,
        url: url,
      }).catch(() => {
        window.open(shareUrl, '_blank', 'noopener,noreferrer')
      })
    } else {
      window.open(shareUrl, '_blank', 'noopener,noreferrer')
    }

    // Track sharing event if GA is available
    if (typeof window !== 'undefined' && typeof window.gtag === 'function') {
      try {
        window.gtag('event', 'share', {
          method: platform,
          content_type: 'article',
          item_id: pathname,
        })
      } catch (error) {
        // Silently fail if gtag is not available
        if (process.env.NODE_ENV === 'development') {
          console.warn('Google Analytics tracking failed:', error)
        }
      }
    }
  }

  const buttonClass = variant === 'horizontal'
    ? 'flex items-center justify-center gap-2 px-4 py-2 rounded-lg transition-colors text-sm font-medium'
    : 'flex flex-col items-center justify-center gap-1 px-3 py-2 rounded-lg transition-colors text-xs font-medium'

  return (
    <div className={`flex ${variant === 'horizontal' ? 'flex-row gap-2' : 'flex-col gap-2'} ${className}`}>
      <button
        onClick={() => handleShare('facebook')}
        className={`${buttonClass} bg-[#1877F2] text-white hover:bg-[#166FE5]`}
        aria-label="שתף בפייסבוק"
      >
        <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24" aria-hidden="true">
          <path d="M24 12.073c0-6.627-5.373-12-12-12s-12 5.373-12 12c0 5.99 4.388 10.954 10.125 11.854v-8.385H7.078v-3.47h3.047V9.43c0-3.007 1.792-4.669 4.533-4.669 1.312 0 2.686.235 2.686.235v2.953H15.83c-1.491 0-1.956.925-1.956 1.874v2.25h3.328l-.532 3.47h-2.796v8.385C19.612 23.027 24 18.062 24 12.073z" />
        </svg>
        {variant === 'horizontal' && <span>פייסבוק</span>}
      </button>

      <button
        onClick={() => handleShare('whatsapp')}
        className={`${buttonClass} bg-[#25D366] text-white hover:bg-[#20BA5A]`}
        aria-label="שתף ב-WhatsApp"
      >
        <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24" aria-hidden="true">
          <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z" />
        </svg>
        {variant === 'horizontal' && <span>WhatsApp</span>}
      </button>

      <button
        onClick={() => handleShare('twitter')}
        className={`${buttonClass} bg-[#1DA1F2] text-white hover:bg-[#1A91DA]`}
        aria-label="שתף בטוויטר"
      >
        <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24" aria-hidden="true">
          <path d="M8.29 20.251c7.547 0 11.675-6.253 11.675-11.675 0-.178 0-.355-.012-.53A8.348 8.348 0 0022 5.92a8.19 8.19 0 01-2.357.646 4.118 4.118 0 001.804-2.27 8.224 8.224 0 01-2.605.996 4.107 4.107 0 00-6.993 3.743 11.65 11.65 0 01-8.457-4.287 4.106 4.106 0 001.27 5.477A4.072 4.072 0 012.8 9.713v.052a4.105 4.105 0 003.292 4.022 4.095 4.095 0 01-1.853.07 4.108 4.108 0 003.834 2.85A8.233 8.233 0 012 18.407a11.616 11.616 0 006.29 1.84" />
        </svg>
        {variant === 'horizontal' && <span>טוויטר</span>}
      </button>

      <button
        onClick={() => handleShare('linkedin')}
        className={`${buttonClass} bg-[#0077B5] text-white hover:bg-[#006399]`}
        aria-label="שתף ב-LinkedIn"
      >
        <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24" aria-hidden="true">
          <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z" />
        </svg>
        {variant === 'horizontal' && <span>LinkedIn</span>}
      </button>

      <button
        onClick={() => handleShare('email')}
        className={`${buttonClass} bg-gray-600 text-white hover:bg-gray-700`}
        aria-label="שתף באימייל"
      >
        <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 8l7.89 5.26a2 2 0 002.22 0L21 8M5 19h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z" />
        </svg>
        {variant === 'horizontal' && <span>אימייל</span>}
      </button>
    </div>
  )
}


