/**
 * פונקציה למיון ביקורות לפי תאריך (החדשות ראשונות)
 */
export function sortReviewsByDate(reviews: ManualReview[]): ManualReview[] {
  return [...reviews].reverse() // אם הוספת מהחדש לישן, זה יציג מהחדש לישן
}

